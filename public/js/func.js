$("#send").click(function(){
    var applydata={
        name:$("input#name").val(),
        number_id:$("input#number").val(),
        class_id:$("#class").val(),
        reason:$("#reason").val(),
        cost:$("#cost").val(),
        address:$("#address").val()
    };
    $.ajax({
        type:"POST",
        url:"apply",
        data:applydata,
        error:function(err){
            alert(err);
        },
        success:function(data){
            alert(data);
        }
    });
});